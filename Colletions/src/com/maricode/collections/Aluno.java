package com.maricode.collections;

public class Aluno {
	
		private String nome;
		private int idade;
		
		public Aluno(String nome) {
				this.setNome(nome);
		}

		public String getNome() {
			return nome;
		}

		public void setNome(String nome) {
			this.nome = nome;
		}

		public int getIdade() {
			return idade;
		}

		public void setIdade(int idade) {
			this.idade = idade;
		}
		
		

}
